/******************************************************************************************/
/**** FUNCTION: intdivfloor                                                            ****/
/******************************************************************************************/
int intdivfloor(int m, int n)
{
    int retval;

    if        ((m >= 0)&&(n >= 0)) { retval = m/n;
    } else if ((m <  0)&&(n <  0)) { retval = (-m)/(-n);
    } else if ((m <  0)&&(n >= 0)) { retval = -((-m-1)/n + 1);
    } else {                         retval = -((m-1)/(-n) + 1);
    }

    return(retval);
}
/******************************************************************************************/
/**** FUNCTION: intdivceil                                                             ****/
/******************************************************************************************/
int intdivceil(int m, int n)
{
    int retval;

    if        ((m >= 0)&&(n >= 0)) { retval = (m+n-1)/n;
    } else if ((m <  0)&&(n <  0)) { retval = (-m-1)/(-n) + 1;
    } else if ((m <  0)&&(n >= 0)) { retval = -((-m)/n);
    } else {                         retval = -((m)/(-n));
    }

    return(retval);
}
